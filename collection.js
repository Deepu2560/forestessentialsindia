function display(proarr)
{
document.querySelector('#body_care_products').textContent = ""
proarr.map(function(el){
    var maindiv = document.createElement('div')
    var div1 = document.createElement('span') 
    var img0 = document.createElement('img')
    img0.setAttribute('src',"https://cdn-icons.flaticon.com/png/512/3870/premium/3870922.png?token=exp=1639563142~hmac=c211f716b578985c0a5f05b4429e170e")
    img0.setAttribute('class',"wishlist")
    img0.setAttribute('alt',"Add to wishlist") 
    div1.append(img0)
    div1.setAttribute('class',"icon-wishlist")
    var img = document.createElement('img') 
    img.setAttribute('src',el.imgUrl)
    div1.append(img)
    var div2 = document.createElement('div') 
    var p = document.createElement('p') 
    p.textContent = el.prodhead
    div2.appendChild(p)
    var div3 = document.createElement('div') 
    div3.setAttribute('class','price')
    var span1 = document.createElement('span')
    span1.textContent = el.prodweight
    div3.appendChild(span1)
    var div4 = document.createElement('div') 
    div4.setAttribute('class','price')
    var span2 = document.createElement('span') 
    span2.textContent = el.prodprice
    div4.appendChild(span2)
    var button = document.createElement('button') 
    button.textContent = "ADD TO BAG"
    button.addEventListener('click',function(){
        cartarr.push(el)
        localStorage.setItem('cart',JSON.stringify(cartarr))
    })
    maindiv.append()
    maindiv.append(div1,div2,div3,div4,button)
    document.querySelector('#body_care_products').append(maindiv)
})
}
